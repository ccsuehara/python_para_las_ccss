def eleva_potencia_cc(a,b):
    '''
    Eleva número "a" a la "b" potencia.
    Insumo (input): 
        a: número
        b: número
    Producto (output):
        resultado: un número
    '''
    z_var = 11
    c = 10 + z_var
    resultado = a**b + c
    return resultado



